    end

    if PlayerData and PlayerData.Materials then
        print("\n=== üéí ‡∏™‡∏£‡∏∏‡∏õ‡πÑ‡∏≠‡πÄ‡∏ó‡πá‡∏°‡πÉ‡∏ô‡∏Å‡∏£‡∏∞‡πÄ‡∏õ‡πã‡∏≤ (Materials) üéí ===")
